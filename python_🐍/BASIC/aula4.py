# Int and Float
# int -> integer
# positive or negative.
print(11) # int
print(-11) # int
print(0)

# float -> floating point number
# type float represents any number
# positive or negative with floating point
print(1.1, 10.11) # float
print(0.0, -1.5)

# function 'type' shows the type of the variable
print(type('string'))
print(type(1))
print(type(1.1), type(-1.1), type(0.0))