# bool (boolean)
# when querying something in a program,
# theres only two possible answers:
# yes (True) or no (False)
# among them ==, is a logic operator that
# question if a value is the same as the other

print(10 == 10) # yes -> True
print(10 == 11) # no -> False
print(type(10 == 10)) 
print(type(True)) 
print(type(False))